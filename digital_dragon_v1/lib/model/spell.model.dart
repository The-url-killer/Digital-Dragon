class Skill {
  String castTime;
  List<String> classes;
  String component;
  String description;
  String duration;
  int level;
  String name;
  String range;
  String school;

  Skill(
      {required this.castTime,
      required this.classes,
      required this.component,
      required this.description,
      required this.duration,
      required this.level,
      required this.name,
      required this.range,
      required this.school});
}
